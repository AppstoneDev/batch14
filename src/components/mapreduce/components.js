export default function Sum(props) {
    return (
        <div>{props.vala + props.valb}</div>
    )
}

export function Difference(props) {
    return (
        <div>{props.vala - props.valb}</div>
    )
}

export function Product(props) {
    return (
        <div>{props.vala * props.valb}</div>
    )
}